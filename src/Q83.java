/**
 * Which of the following expressions will evaluate to 7?
 */

public class Q83 {
    public static void main(String[] args) {

        /** OPTION A */
        System.out.println(2 + 4 * 3- 7);

        /** OPTION B */
        System.out.println((2 + 4) * (3 - 7));

        /** OPTION C */
        System.out.println(2 + (4 * 3) - 7);

        /** OPTION D */
        System.out.println(((2 + 4) * 3) - 7);

    }
}
